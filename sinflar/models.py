from django.db import models

class Sinf_1(models.Model):
	ism = models.CharField(max_length=100, null=True)
	familya = models.CharField(max_length=100, null=True)
	sharif = models.CharField(max_length=100, null=True)
	info  = models.TextField(null=True)
	image = models.ImageField(upload_to='sinf-1', null=True)

	def __str__(self):
		return self.ism

